    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        ConexionDB conexion = new ConexionDB();
        
        //Preparo la consulta
        PreparedStatement pstm = null;   
       
        //He de extraer todos los valores de los campos Para a√±adirlos a la base de datos
        String user = this.nombreUsuario.getText();
        String pass = this.passUsuario.getText();
        String email = this.correoUsuario.getText();
        String equipo = this.equipoUsuario.getText();
        String fech = this.fechUsuario.getText();
        String servidor = this.serverUsuario.getSelectedItem().toString();
        
        String sqlQuery = "INSERT INTO cuentapertenece(nombre,idequipo,contrasena,correo,fec_nac,servidor,saldo,karma) VALUES(?,?,?,?,?,?,?,?)";
               
        //Preparo la sentencia a ejecutar.
        try{
        pstm = conexion.con.prepareStatement(sqlQuery);
        pstm.setString(1, user);
        pstm.setString(2, equipo);
        pstm.setString(3, pass);
        pstm.setString(4, email);
        pstm.setString(5, fech);
        pstm.setString(6, servidor);
        pstm.setString(7, "0");
        pstm.setString(8, "0");
        
        int resultado = pstm.executeUpdate();
        
        if(resultado ==1){
            System.out.print("Fila insertada correctamente");
            JOptionPane.showMessageDialog(null, "Se ha insertado la fila correctamente", "Alerta ", JOptionPane.INFORMATION_MESSAGE);
        }else{
            throw new RuntimeException("No se pudo insertar la fila");
        }
            
        
        
        
        }catch(SQLException ex){
            ex.printStackTrace(System.out);
            throw new RuntimeException(ex);
        }finally{
            
            try{
                if(pstm!=null) pstm.close();
                if(conexion.con!=null) conexion.cerrarConexionDB();
            }catch(SQLException ex){
                ex.printStackTrace(System.out);
                throw new RuntimeException(ex);
            }
        }
        
        //lIMPIO LOS CAMPOS POR SI QUIERO METER M√ÅS USUARIOS
        
        this.nombreUsuario.setText("");
        this.passUsuario.setText("");
        this.correoUsuario.setText("");
        this.equipoUsuario.setText("");
        this.fechUsuario.setText("");
        
            
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

